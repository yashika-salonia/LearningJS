//Function with parameters

//Sum function with parameters defined
function sum(x,y,z){
    let s=x+y+z;
    return "The sum of two numbers are " + s;
}
console.log(sum(20,70,64));

//Division function
function div(p,q){
    let d=p/q;
    return "The value of div of two digits are "+ d;
}
console.log(div(220,6));

