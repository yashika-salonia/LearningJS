//Function without parameters

//Square function without parameters
function square(){
    var x = 2;
    return "The square of two numbers is " + x*x;
}
console.log(square());

//Product function without parameters
function product(){
    var a=10;
    var b=3;
    var c=7;
    var prod=a*b*c;
    return "The product of two numbers is " + prod;
}
console.log(product());
